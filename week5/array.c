#include<stdio.h>

int main()
{ 
	int A[8]={24,56,78,76,34,2,56,78};
	int option;

	printf("\nIn which order you want to see the elements of the array\n ");
	printf("\tEnter 0 for Simple order.\n");	
	printf("\tEnter 1 for Inverse order.\n");

	scanf("%d",&option);
	
	switch(option)
	{
	case 0: 
		for(int i=0;i<8;i++)
		{
		printf("A[%d]=%d\n",i,A[i]);		
		}
		break;

	case 1:	
		for(int i=7;i>=0;i--)
		{
		printf("A[%d]=%d\n",i,A[i]);		
		}
		break;

	}
     return 0;
}
