//finding maxm number between n numbers
#include<stdio.h>

int main()
{ 
	int n,a,max,counter;

	printf("How many numbers you want to compare?:");
	scanf(" %d",&n);

	if(n==0)
	{
	printf("No numbers to compare!");	
	}

	else
	{
		printf("Please enter the value you want to compare:");
		scanf(" %d",&a);
		//intialization
		max=a;
		counter=1;
		while(counter<n)
		{
		printf("Please enter the value you want to compare:");
		scanf(" %d",&a);

			if(a>max)		
			{
			max=a;		
			}
		counter++;	
		}
		printf("maxm of entered %d numbers is %d\n",n,max);
	}
		
	return 0;
}
